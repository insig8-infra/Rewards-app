import { existsSync, readFileSync, writeFileSync } from "node:fs";

const sourcePath = ".env.local";
const outputPath = ".env.railway.local";

const requiredKeys = [
  "NODE_ENV",
  "HOST",
  "NEON_CONNECTION_STRING",
  "JWT_SECRET",
  "QR_TOKEN_SECRET",
  "BUSY_INTEGRATION_CLIENT_ID",
  "BUSY_INTEGRATION_API_KEY",
  "MEDIA_STORAGE_MODE",
  "SMS_PROVIDER",
];

const optionalKeys = ["CORS_ORIGIN"];

const defaults = new Map([
  ["NODE_ENV", "production"],
  ["HOST", "0.0.0.0"],
  ["MEDIA_STORAGE_MODE", "local"],
  ["SMS_PROVIDER", "mock"],
]);

if (!existsSync(sourcePath)) {
  throw new Error(`${sourcePath} is missing.`);
}

const env = new Map(defaults);

for (const line of readFileSync(sourcePath, "utf8").split(/\r?\n/)) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith("#")) {
    continue;
  }

  const separator = trimmed.indexOf("=");
  if (separator < 0) {
    continue;
  }

  const key = trimmed.slice(0, separator).trim();
  let value = trimmed.slice(separator + 1).trim();
  if ((value.startsWith("\"") && value.endsWith("\"")) || (value.startsWith("'") && value.endsWith("'"))) {
    value = value.slice(1, -1);
  }
  env.set(key, value);
}

const missing = requiredKeys.filter((key) => !env.get(key));
if (missing.length) {
  throw new Error(`Missing required Railway variables in ${sourcePath}: ${missing.join(", ")}`);
}

const lines = [
  "# Generated by tools/prepare-railway-variables.mjs",
  "# Import/copy these values into Railway service Variables. Do not commit this file.",
  "",
];

for (const key of requiredKeys) {
  lines.push(`${key}=${env.get(key)}`);
}

for (const key of optionalKeys) {
  const value = env.get(key);
  if (value) {
    lines.push(`${key}=${value}`);
  }
}

writeFileSync(outputPath, `${lines.join("\n")}\n`);

console.log(`Wrote ${outputPath} with ${requiredKeys.length + optionalKeys.filter((key) => env.get(key)).length} Railway variables.`);
console.log("This file is ignored by .gitignore and should be copied/imported into Railway service Variables.");
